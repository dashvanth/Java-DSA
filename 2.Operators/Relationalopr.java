public class Relationalopr {
    public static void main(String args[]) {

        // Relational operators
        int a = 10, b = 20, c = 30;
        System.out.println("is a equals to b=" + (a == b));
        System.out.println("is a not equal to b=" + (a != b));
        System.out.println("is a greater than b=" + (a > b));
        System.out.println("is a is lesser than b=" + (a < b));
        System.out.println("is a is greater or equal to b=" + (a >= b));
        System.out.println("is a lesser than or equal to b=" + (a <= b));

    }
}
