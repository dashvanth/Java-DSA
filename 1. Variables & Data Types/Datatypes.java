// Dataypes in java 
public class Datatypes {
    public static void main(String args[]) {
        byte b = 8;
        System.out.println(b);
        char ch = 'a';
        System.out.println(ch);
        boolean var = false;
        System.out.println(var);
        float price = 10.5f;
        System.out.println(price);
        int number = 25;
        System.out.println(number);
        // long
        // double
        short n = 240;

        // Dataypes sizes

        // int=4bytes
        // char=2bytes
        // float=4bytes
        // byte=1byte
        // boolean=2bytes
        // long=8bytes
        // double=8bytes

    }
}
