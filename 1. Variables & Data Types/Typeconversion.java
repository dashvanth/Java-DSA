
//Type or widening or implicit conversion
import java.util.*;

public class Typeconversion {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        // int number = sc.nextFloat(); // int cannot be converted into float
        float number = sc.nextInt(); // float can be converted into int
        System.out.println(number);

        // byte->short->int->float->long->double->
    }
}